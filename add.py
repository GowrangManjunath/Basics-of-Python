#How to add two numbers in python
a = int(input("Enter your first number :")) #int/float is the data type ; input is the command used for taking input from user
b = int(input("Enter your second number:")) #int/float is the data type ; input is the command used for taking input from user
c = a+b
print("The Addition of your numbers are", c) #whatever is written between the "" are taken as string and will be printed as it is, c will print the value of c
# in this code the user will not be able to enter decimal number as the input is for interger data type and not float data type. float refers to decimal numbers. If you want to add decimal numbers use  float instead of int in your code
